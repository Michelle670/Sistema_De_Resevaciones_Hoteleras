     private void actualizarTabla() 
    {
        DefaultTableModel modelo = (DefaultTableModel) jtPais.getModel();
        modelo.setRowCount(0);

        Connection conn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;

        try {
            conn = ConexionBD.getConnection();
            String sql = "SELECT id_pais,codigo,nombre FROM pais";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {
                Object[] fila = {
                    rs.getInt("id_pais"),
                    rs.getString("codigo"),
                    rs.getString("nombre"),
                   
                };
                modelo.addRow(fila);
            }
            
            jtPais.setModel(modelo);
        } catch (SQLException e) {
            System.out.println("Error al cargar los paises: " + e.getMessage());
        } finally {
            try { if (rs != null) rs.close(); } catch (Exception e) {}
            try { if (ps != null) ps.close(); } catch (Exception e) {}
            // NO cerramos conn porque es conexión global
        }
    }
